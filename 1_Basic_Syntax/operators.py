num_1 = int(input('enter first number : '))
num_2 = int(input('enter second number : '))

print(num_1 + num_2)
print(num_1 - num_2)
print(num_1 * num_2)
print(num_1 / num_2)
print(num_1 // num_2)
print(num_1 ** num_2)
print(num_1 % num_2)

print(num_1 > num_2)
print(num_1 < num_2)
print(num_1 >= num_2)
print(num_1 <= num_2)
print(num_1 != num_2)
print(num_1 == num_2)

# Logical Operators
print(num_1 and num_2)
print(num_1 or num_2)
print(not num_2)

# Identity Operators
print(num_1 is num_2)
print(num_1 is not num_2)

# Membership Operators
print(num_1 in num_2)
print(num_1 not in num_2)